// Here we will perform some actions or function on string

var name = 'naresh';

console.log('the length of the name : ', name.length)  // for checking the length of a string

// indexOf  : will return the index of the length

console.log(name.indexOf('a')); 

// slice : this function will take the index and return a sub string

console.log(name.slice(2,6)); 

// replace : this function take a string and replace this

console.log(name.replace('nar','Rak'));   

// toUpperCase : this will change is uppercase

console.log(name.toUpperCase());

// toLowerCase : this will change is uppercase

console.log(name.toLowerCase());

// charAt : this fuction take index and return a char

console.log(name.charAt(5));  // if you will nothing pass then this will return the first char of string

// split : this is same like python saprate with '' and , and more...
console.log(name.split(""));

